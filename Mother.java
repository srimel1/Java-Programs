

public class Mother {

	int valueA,valueB;

	public Mother() {

		valueA=0;

		valueB=0;

	}

	public Mother(int n,int m) {

		valueA=n;

		valueB = m;

	}

	public int getValueA() {

		return valueA;

	}

	public void setValueA(int valueA) {

		this.valueA = valueA;

	}

	public int getValueB() {

		return valueB;

	}

	public void setValueB(int valueB) {

		this.valueB = valueB;

	}

	public int Add(int a,int b,int c) {

		return a*b+c;

	}

}




